    public void abrir(String nombreOperador) {
        try {
            File path = new File(nombreOperador + ".pdf");
            Desktop.getDesktop().open(path);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex, "Atenci√≥n", 2);
        }
    }
    
